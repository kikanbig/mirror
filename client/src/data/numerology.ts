function getDefaultCompatibility(num1: number, num2: number): CompatibilityResult {
  const base1 = MASTER_NUMBERS.has(num1) ? reduceToSingleDigit(num1 === 11 ? 2 : num1 === 22 ? 4 : 6) : num1;
  const base2 = MASTER_NUMBERS.has(num2) ? reduceToSingleDigit(num2 === 11 ? 2 : num2 === 22 ? 4 : 6) : num2;

  const key = pairKey(base1, base2);
  const baseResult = COMPATIBILITY_DATA[key];

  if (baseResult) {
    const bonus = (MASTER_NUMBERS.has(num1) ? 5 : 0) + (MASTER_NUMBERS.has(num2) ? 5 : 0);
    return {
      ...baseResult,
      score: Math.min(100, baseResult.score + bonus),
      description: baseResult.description + ' Присутствие мастер-числа усиливает духовную глубину этого союза.',
    };
  }

  return {
    score: 60,
    description: 'Уникальное сочетание чисел, которое требует индивидуального подхода и взаимного уважения.',
    strengths: ['Необычность союза', 'Потенциал для роста'],
    challenges: ['Необходимость понимания различий', 'Нестандартная динамика'],
    advice: 'Изучайте друг друга и принимайте уникальность вашей пары. Каждый союз — это отдельная Вселенная.',
  };
}

export function calculateCompatibility(num1: number, num2: number): CompatibilityResult {
  const key = pairKey(num1, num2);
  if (COMPATIBILITY_DATA[key]) {
    return COMPATIBILITY_DATA[key];
  }
  return getDefaultCompatibility(num1, num2);
}

