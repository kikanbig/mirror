export interface PsychomatrixResult {
  cells: Record<number, number>;
  workingNumbers: [number, number, number, number];
  allDigits: string;
  lines: {
    row1: number; row2: number; row3: number;
    col1: number; col2: number; col3: number;
    diag1: number; diag2: number;
  };
}

function simpleReduce(n: number): number {
  n = Math.abs(n);
  while (n > 9) {
    let s = 0;
    while (n > 0) { s += n % 10; n = Math.floor(n / 10); }
    n = s;
  }
  return n;
}

export function calculatePsychomatrix(birthDate: Date): PsychomatrixResult {
  const day = birthDate.getDate();
  const month = birthDate.getMonth() + 1;
  const year = birthDate.getFullYear();

  const dateStr = `${day}${month}${year}`;
  const digitSum = dateStr.split('').reduce((a, c) => a + parseInt(c, 10), 0);

  const w1 = digitSum;
  const w2 = simpleReduce(w1);

  const firstDigitOfDay = day >= 10 ? Math.floor(day / 10) : day;
  const w3 = Math.abs(w1 - firstDigitOfDay * 2);
  const w4 = simpleReduce(w3);

  const fullStr = dateStr + w1.toString() + w2.toString() + w3.toString() + w4.toString();

  const cells: Record<number, number> = {};
  for (let i = 1; i <= 9; i++) cells[i] = 0;
  for (const ch of fullStr) {
    const d = parseInt(ch, 10);
    if (d >= 1 && d <= 9) cells[d]++;
  }

  const lines = {
    row1: cells[1] + cells[4] + cells[7],
    row2: cells[2] + cells[5] + cells[8],
    row3: cells[3] + cells[6] + cells[9],
    col1: cells[1] + cells[2] + cells[3],
    col2: cells[4] + cells[5] + cells[6],
    col3: cells[7] + cells[8] + cells[9],
    diag1: cells[1] + cells[5] + cells[9],
    diag2: cells[3] + cells[5] + cells[7],
  };

  return {
    cells,
    workingNumbers: [w1, w2, w3, w4],
    allDigits: fullStr.replace(/0/g, ''),
    lines,
  };
}

