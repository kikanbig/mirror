const RUSSIAN_LETTER_VALUES: Record<string, number> = {
  А: 1, И: 1, С: 1, Ъ: 1,
  Б: 2, Й: 2, Т: 2, Ы: 2,
  В: 3, К: 3, У: 3, Ь: 3,
  Г: 4, Л: 4, Ф: 4, Э: 4,
  Д: 5, М: 5, Х: 5, Ю: 5,
  Е: 6, Н: 6, Ц: 6, Я: 6,
  Ё: 7, О: 7, Ч: 7,
  Ж: 8, П: 8, Ш: 8,
  З: 9, Р: 9, Щ: 9,
};

const RUSSIAN_VOWELS = new Set(['А', 'Е', 'Ё', 'И', 'О', 'У', 'Ы', 'Э', 'Ю', 'Я']);

const RUSSIAN_CONSONANTS = new Set([
  'Б', 'В', 'Г', 'Д', 'Ж', 'З', 'Й', 'К', 'Л', 'М',
  'Н', 'П', 'Р', 'С', 'Т', 'Ф', 'Х', 'Ц', 'Ч', 'Ш', 'Щ',
]);

const MASTER_NUMBERS = new Set([11, 22, 33]);

// ═══════════════════════════════════════════════
//  Вспомогательные функции
// ═══════════════════════════════════════════════

export function reduceToSingleDigit(num: number): number {
  num = Math.abs(num);
  while (num > 9 && !MASTER_NUMBERS.has(num)) {
    let sum = 0;
    while (num > 0) {
      sum += num % 10;
      num = Math.floor(num / 10);
    }
    num = sum;
  }
  return num;
}

function sumDigits(n: number): number {
  let sum = 0;
  n = Math.abs(n);
  while (n > 0) {
    sum += n % 10;
    n = Math.floor(n / 10);
  }
  return sum;
}

function letterValue(letter: string): number {
  return RUSSIAN_LETTER_VALUES[letter.toUpperCase()] ?? 0;
}

function nameToLetters(fullName: string): string[] {
  return fullName
    .toUpperCase()
    .replace(/\s+/g, '')
    .split('')
    .filter((ch) => ch in RUSSIAN_LETTER_VALUES);
}

// ═══════════════════════════════════════════════
//  Расчёт основных чисел
// ═══════════════════════════════════════════════

export function calculateLifePathNumber(birthDate: Date): number {
  const day = birthDate.getDate();
  const month = birthDate.getMonth() + 1;
  const year = birthDate.getFullYear();

  const dayReduced = reduceToSingleDigit(sumDigits(day));
  const monthReduced = reduceToSingleDigit(sumDigits(month));
  const yearReduced = reduceToSingleDigit(sumDigits(year));

  return reduceToSingleDigit(dayReduced + monthReduced + yearReduced);
}

export function calculateSoulNumber(fullName: string): number {
  const letters = nameToLetters(fullName);
  const vowelSum = letters
    .filter((ch) => RUSSIAN_VOWELS.has(ch))
    .reduce((acc, ch) => acc + letterValue(ch), 0);
  return reduceToSingleDigit(vowelSum);
}

export function calculateDestinyNumber(fullName: string): number {
  const letters = nameToLetters(fullName);
  const total = letters.reduce((acc, ch) => acc + letterValue(ch), 0);
  return reduceToSingleDigit(total);
}

export function calculatePersonalityNumber(fullName: string): number {
  const letters = nameToLetters(fullName);
  const consonantSum = letters
    .filter((ch) => RUSSIAN_CONSONANTS.has(ch))
    .reduce((acc, ch) => acc + letterValue(ch), 0);
  return reduceToSingleDigit(consonantSum);
}

export function calculatePersonalYear(birthDate: Date, currentYear?: number): number {
  const year = currentYear ?? new Date().getFullYear();
  const day = birthDate.getDate();
  const month = birthDate.getMonth() + 1;

  const sum = sumDigits(day) + sumDigits(month) + sumDigits(year);
  return reduceToSingleDigit(sum);
}

export function calculatePersonalMonth(birthDate: Date, currentDate?: Date): number {
  const now = currentDate ?? new Date();
  const personalYear = calculatePersonalYear(birthDate, now.getFullYear());
  const calendarMonth = now.getMonth() + 1;
  return reduceToSingleDigit(personalYear + sumDigits(calendarMonth));
}

export function calculatePersonalDay(birthDate: Date, currentDate?: Date): number {
  const now = currentDate ?? new Date();
  const personalMonth = calculatePersonalMonth(birthDate, now);
  const calendarDay = now.getDate();
  return reduceToSingleDigit(personalMonth + sumDigits(calendarDay));
}

