// Replicate reduceToArcana from client-side for server calculation
function reduceToArcana(n: number): number {
  while (n > 22) n = Math.floor(n / 10) + (n % 10);
  return n === 0 ? 22 : n;
}

interface MatrixNumbers {
  a: number; b: number; c: number; d: number; center: number;
  ab: number; bc: number; cd: number; da: number;
  aCenter: number; bCenter: number; cCenter: number; dCenter: number;
  sky: number; earth: number; male: number; female: number;
  socialCenter: number; spiritual: number;
  karmicTail: [number, number, number];
  yearEnergy: number; currentAge: number;
  chakras: Array<{ nameRu: string; physical: number; energy: number; emotions: number }>;
}

function calculateMatrix(birthDate: Date): MatrixNumbers {
  const r = reduceToArcana;
  const day = birthDate.getDate();
  const month = birthDate.getMonth() + 1;
  const year = birthDate.getFullYear();

  const a = r(day);
  const b = r(month);
  const yearDigits = year.toString().split('').reduce((s, ch) => s + parseInt(ch, 10), 0);
  const c = r(yearDigits);
  const d = r(a + b + c);
  const center = r(a + b + c + d);

  const ab = r(a + b), bc = r(b + c), cd = r(c + d), da = r(d + a);
  const aCenter = r(a + center), bCenter = r(b + center);
  const cCenter = r(c + center), dCenter = r(d + center);

  const cCd = r(c + cd);

  const sky = a;
  const earth = r(a + c);
  const male = r(ab + cd);
  const female = r(bc + da);
  const socialCenter = r(male + female);
  const spiritual = r(center + socialCenter);

  const kt1 = d, kt2 = r(d + center), kt3 = r(kt1 + kt2);

  const now = new Date();
  const currentAge = now.getFullYear() - year - (now < new Date(now.getFullYear(), month - 1, day) ? 1 : 0);
  const yearEnergy = r(currentAge + a);

  const chakras = [
    { nameRu: 'Сахасрара', physical: a, energy: b, emotions: ab },
    { nameRu: 'Аджна', physical: ab, energy: r(ab + cd), emotions: cd },
    { nameRu: 'Вишудха', physical: b, energy: bCenter, emotions: r(bCenter + cCd) },
    { nameRu: 'Анахата', physical: bCenter, energy: a, emotions: cCd },
    { nameRu: 'Манипура', physical: center, energy: center, emotions: d },
    { nameRu: 'Свадхистана', physical: b, energy: dCenter, emotions: b },
    { nameRu: 'Муладхара', physical: a, energy: d, emotions: da },
  ];

  return {
    a, b, c, d, center, ab, bc, cd, da,
    aCenter, bCenter, cCenter, dCenter,
    sky, earth, male, female, socialCenter, spiritual,
    karmicTail: [kt1, kt2, kt3],
    yearEnergy, currentAge, chakras,
  };
}

