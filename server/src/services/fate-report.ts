async function generateChapter(prompt: string): Promise<string> {
  const result = await groq.chat.completions.create({
    model: GROQ_MODEL,
    messages: [{ role: 'user', content: prompt }],
    temperature: 0.8,
    max_tokens: 4096,
  });
  return result.choices[0]?.message?.content || '';
}

export async function generateFateReport(
  birthDate: Date,
  userId: string,
  telegramId?: number | bigint,
  lang = 'ru',
): Promise<{ id: string; chapters: Record<string, Chapter> }> {
  const matrix = calculateMatrix(birthDate);
  const chapterPrompts = buildChapterPrompts(matrix, birthDate, lang);

  const chapters: Record<string, Chapter> = {};
  const BATCH_SIZE = 3;

  for (let i = 0; i < chapterPrompts.length; i += BATCH_SIZE) {
    const batch = chapterPrompts.slice(i, i + BATCH_SIZE);
    const results = await Promise.allSettled(
      batch.map(ch => generateChapter(ch.prompt))
    );

    for (let j = 0; j < batch.length; j++) {
      const ch = batch[j];
      const result = results[j];
      chapters[ch.id.toString()] = {
        id: ch.id,
        title: ch.title,
        content: result.status === 'fulfilled'
          ? result.value
          : FAIL_MSG[lang] || FAIL_MSG.ru,
      };
    }

    const done = Math.min(i + BATCH_SIZE, chapterPrompts.length);
    console.log(`[FateReport] Generated ${done}/${chapterPrompts.length} chapters for user ${userId}`);

    // Pause between batches to respect Groq rate limits
    if (i + BATCH_SIZE < chapterPrompts.length) {
      await new Promise(resolve => setTimeout(resolve, 2000));
    }
  }

  const wordCount = Object.values(chapters).reduce(
    (sum, ch) => sum + ch.content.split(/\s+/).length, 0
  );

  const report = await prisma.fateReport.upsert({
    where: { userId_birthDate: { userId, birthDate } },
    create: {
      userId,
      birthDate,
      chapters: chapters as any,
      wordCount,
      status: 'ready',
    },
    update: {
      chapters: chapters as any,
      wordCount,
      status: 'ready',
    },
  });

  console.log(`[FateReport] Complete: ${wordCount} words, ${Object.keys(chapters).length} chapters`);

  return { id: report.id, chapters };
}
