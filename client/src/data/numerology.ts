const KARMIC_DEBT_NUMBERS = [13, 14, 16, 19];

function rawSumBeforeReduce(n: number): number[] {
  const intermediates: number[] = [];
  n = Math.abs(n);
  while (n > 9 && !MASTER_NUMBERS.has(n)) {
    intermediates.push(n);
    let s = 0;
    while (n > 0) { s += n % 10; n = Math.floor(n / 10); }
    n = s;
  }
  return intermediates;
}

export interface KarmicDebtInfo {
  number: number;
  source: string;
}

export function findKarmicDebts(birthDate: Date, fullName?: string): KarmicDebtInfo[] {
  const debts: KarmicDebtInfo[] = [];
  const seen = new Set<number>();

  const addIfKarmic = (intermediates: number[], source: string) => {
    for (const n of intermediates) {
      if (KARMIC_DEBT_NUMBERS.includes(n) && !seen.has(n)) {
        seen.add(n);
        debts.push({ number: n, source });
      }
    }
  };

  const day = birthDate.getDate();
  const month = birthDate.getMonth() + 1;
  const year = birthDate.getFullYear();
  const daySum = sumDigits(day);
  const monthSum = sumDigits(month);
  const yearSum = sumDigits(year);
  const lpRaw = daySum + monthSum + yearSum;

  addIfKarmic([daySum, ...rawSumBeforeReduce(daySum)], 'День рождения');
  addIfKarmic([lpRaw, ...rawSumBeforeReduce(lpRaw)], 'Число Жизненного Пути');

  if (KARMIC_DEBT_NUMBERS.includes(day)) {
    if (!seen.has(day)) { seen.add(day); debts.push({ number: day, source: 'День рождения (прямой)' }); }
  }

  if (fullName) {
    const letters = nameToLetters(fullName);
    const total = letters.reduce((acc, ch) => acc + letterValue(ch), 0);
    addIfKarmic([total, ...rawSumBeforeReduce(total)], 'Число Судьбы');

    const vowels = letters.filter((ch) => RUSSIAN_VOWELS.has(ch)).reduce((a, c) => a + letterValue(c), 0);
    addIfKarmic([vowels, ...rawSumBeforeReduce(vowels)], 'Число Души');

    const consonants = letters.filter((ch) => RUSSIAN_CONSONANTS.has(ch)).reduce((a, c) => a + letterValue(c), 0);
    addIfKarmic([consonants, ...rawSumBeforeReduce(consonants)], 'Число Личности');
  }

  return debts;
}

