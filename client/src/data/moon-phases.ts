export interface MoonPhaseInfo {
  phase: string;
  phaseRu: string;
  emoji: string;
  description: string;
  energy: string;
  recommendations: string[];
  rituals: string[];
  tarotConnection: string;
}

const KNOWN_NEW_MOON = Date.UTC(2000, 0, 6, 18, 14, 0);
const LUNATION_CYCLE = 29.530588853;

export function getMoonPhase(date?: Date): {
  phase: string;
  phaseRu: string;
  illumination: number;
  age: number;
  emoji: string;
  isWaxing: boolean;
} {
  const now = date ?? new Date();
  const diffMs = now.getTime() - KNOWN_NEW_MOON;
  const diffDays = diffMs / (1000 * 60 * 60 * 24);
  const age = ((diffDays % LUNATION_CYCLE) + LUNATION_CYCLE) % LUNATION_CYCLE;

  const fraction = age / LUNATION_CYCLE;
  const illumination = (1 - Math.cos(2 * Math.PI * fraction)) / 2;
  const isWaxing = fraction < 0.5;

  let phase: string;
  let phaseRu: string;
  let emoji: string;

  if (fraction < 0.0625) {
    phase = 'new_moon';
    phaseRu = 'Новолуние';
    emoji = '🌑';
  } else if (fraction < 0.1875) {
    phase = 'waxing_crescent';
    phaseRu = 'Растущий серп';
    emoji = '🌒';
  } else if (fraction < 0.3125) {
    phase = 'first_quarter';
    phaseRu = 'Первая четверть';
    emoji = '🌓';
  } else if (fraction < 0.4375) {
    phase = 'waxing_gibbous';
    phaseRu = 'Растущая луна';
    emoji = '🌔';
  } else if (fraction < 0.5625) {
    phase = 'full_moon';
    phaseRu = 'Полнолуние';
    emoji = '🌕';
  } else if (fraction < 0.6875) {
    phase = 'waning_gibbous';
    phaseRu = 'Убывающая луна';
    emoji = '🌖';
  } else if (fraction < 0.8125) {
    phase = 'last_quarter';
    phaseRu = 'Последняя четверть';
    emoji = '🌗';
  } else if (fraction < 0.9375) {
    phase = 'waning_crescent';
    phaseRu = 'Убывающий серп';
    emoji = '🌘';
  } else {
    phase = 'new_moon';
    phaseRu = 'Новолуние';
    emoji = '🌑';
  }

  return {
    phase,
    phaseRu,
    illumination: Math.round(illumination * 1000) / 1000,
    age: Math.round(age * 100) / 100,
    emoji,
    isWaxing,
  };
}

